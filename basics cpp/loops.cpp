#include <bits/stdc++.h>
using namespace std;

int main () {
    for (int i = 1; i <= 5; i = i +1) {
        cout << "manas"  << i << endl;
    }

    for (int i = 5; i >= 1; i = i-1){
        cout << "manas" << i << endl;
    }

    int i = 1;
    while (i <= 5) {
        cout << "manas" << i << endl;
        i = i + 1;
    }

    int j = 1;
    do {
        cout << "manas" << j << endl;
        j = j + 1;
    } while (j <= 5);
    




    return 0;
}